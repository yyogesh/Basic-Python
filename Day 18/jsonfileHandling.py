# python dictionary
person = {
    "name": "Asabeneh",
    "country": "Finland",
    "city": "Helsinki",
    "skills": ["JavaScrip", "React", "Python"]
}
# let's convert it to  json
# indent could be 2, 4, 8. It beautifies the json
person_json = json.dumps(person, indent=4)
print(type(person_json))
print(person_json)


